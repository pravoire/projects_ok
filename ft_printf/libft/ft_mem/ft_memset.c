#include "ft_mem.h"

void		*ft_memset(void *b, int c, size_t len)
{
	b += (len - 1);
	while (len--)
	{
		*(t_uchar *)b = (t_uchar)c;
		if (len)
			b--;
	}
	return (b);
}
